// 조사 자동 선택 유틸 검증 — 한글 받침/숫자/영문 라벨/실제 게임 케이스.
import { josa } from '../js/util/josa.js';

let fail = 0;
const eq = (got, want, m) => { const ok = got === want; console.log(`  ${ok ? '✓' : `✗ FAIL(got "${got}")`} ${m}`); if (!ok) fail++; };

console.log('=== 조사 자동 선택 (josa) ===\n');

// 한글 받침
eq(josa('압박 강화', '이', '가'), '압박 강화가', '강화(받침X) → 가');
eq(josa('측면 봉쇄', '이', '가'), '측면 봉쇄가', '봉쇄(받침X) → 가');
eq(josa('역습 위험', '이', '가'), '역습 위험이', '위험(ㅁ받침) → 이');
eq(josa('수비', '은', '는'), '수비는', '수비(받침X) → 는');
eq(josa('블록', '은', '는'), '블록은', '블록(ㄱ받침) → 은');

// 숫자 (1~11)
eq(josa('8', '이', '가'), '8이', '8=팔(ㄹ받침) → 이');
eq(josa('9', '이', '가'), '9가', '9=구(받침X) → 가');
eq(josa('2', '이', '가'), '2가', '2=이(받침X) → 가');

// 영문 포지션 라벨
eq(josa('ST', '이', '가'), 'ST가', 'ST=…티(받침X) → 가');
eq(josa('GK', '이', '가'), 'GK가', 'GK=…케이(받침X) → 가');
eq(josa('LCB', '이', '가'), 'LCB가', 'LCB=…비(받침X) → 가');
eq(josa('LM', '이', '가'), 'LM이', 'LM=…엠(ㅁ받침) → 이');
eq(josa('LW', '이', '가'), 'LW가', 'LW=…더블유(받침X) → 가');

// name() 형식 "label(num)" — 끝 ) 제거 후 숫자 판정
eq(josa('ST(9)', '이', '가'), 'ST(9)가', 'ST(9) → 9=구 → 가');
eq(josa('8(10)', '이', '가'), '8(10)이', '8(10) → 끝 0=영(ㅇ받침) → 이');
eq(josa('6(6)', '이', '가'), '6(6)이', '6(6) → 6=육(ㄱ받침) → 이');

// 받침 없는 빈/기호 안전
eq(josa('', '이', '가'), '가', '빈 문자열 → 받침 없음');
eq(josa('동료', '이', '가'), '동료가', '동료(받침X) → 가');

console.log(fail === 0 ? '\n✅ 조사 유틸 전 항목 통과' : `\n❌ ${fail}건 실패`);
process.exit(fail === 0 ? 0 : 1);
