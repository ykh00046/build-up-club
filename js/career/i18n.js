let lang = detectLang();
function detectLang() {
  // Non-browser (Node test scripts, tooling): default to the source language
  // 'ko' so engine display text matches its hardcoded-Korean test assertions.
  // Real browsers (window defined) keep the query/localStorage/navigator path.
  if (typeof window === 'undefined') return 'ko';
  try {
    const q = new URLSearchParams(location.search).get('lang');
    if (q && DICT[q]) return q;
    const saved = localStorage.getItem('buc-lang');
    if (saved && DICT[saved]) return saved;
  } catch (e) { /* ignore */ }
  const n = ((typeof navigator !== 'undefined' && navigator.language) || 'en').toLowerCase();
  return n.startsWith('ko') ? 'ko' : 'en';
}

export function t(key) { return DICT[lang][key] ?? DICT.ko[key] ?? key; }
export function getLang() { return lang; }

// Resolve a bilingual data value. Pass a plain string (returned as-is) or a
// { ko, en } object (returns the current language, falling back ko→en). Lets
// data files (scenarios, scouting, set-pieces) hold both languages inline
// without routing every field through the key dictionary.
export function loc(v) {
  if (v && typeof v === 'object' && ('ko' in v || 'en' in v)) return v[lang] ?? v.ko ?? v.en;
  return v;
}
export function toggleLang() {
  lang = lang === 'ko' ? 'en' : 'ko';
  try { localStorage.setItem('buc-lang', lang); } catch (e) { /* ignore */ }
  return lang;
}

// Walk the DOM and fill text/attributes from data-i18n* attributes. Safe to call
// repeatedly (idempotent) — re-render static labels after a language switch.
export function applyStaticI18n(root = document) {
  for (const el of root.querySelectorAll('[data-i18n]')) el.textContent = t(el.getAttribute('data-i18n'));
  for (const el of root.querySelectorAll('[data-i18n-html]')) el.innerHTML = t(el.getAttribute('data-i18n-html'));
  for (const el of root.querySelectorAll('[data-i18n-title]')) el.title = t(el.getAttribute('data-i18n-title'));
  for (const el of root.querySelectorAll('[data-i18n-aria]')) el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
}
